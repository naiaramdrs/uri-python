teste = int(input())
for i in range(teste):
    jogador1 = input()
    jogador2 = input()

    if jogador1 == 'ataque':
        if jogador2 == 'pedra' or jogador2 == 'papel':
            print('Jogador 1 venceu')
        elif jogador2 == 'ataque':
            print('Aniquilacao mutua')
            
    elif jogador1 == 'pedra':
        if jogador2 == 'ataque':
            print('Jogador 2 venceu')
        elif jogador2 == 'papel':
            print('Jogador 1 venceu')
        elif jogador2 == 'pedra':
            print('Sem ganhador')
            
    elif jogador1 == 'papel':
        if jogador2 == 'ataque' or jogador2 == 'pedra':
            print('Jogador 2 venceu')
        elif jogador2 == 'papel':
            print('Ambos venceram')
